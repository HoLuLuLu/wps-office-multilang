function viewsample1() {
	if (this.document.all.sample1.style.display=="none")
	{
		this.document.all.mydiv1.className = "charrlose";
		this.document.all.mydiv1.className = "charr2";
		this.document.all.sample1.style.display="block";
	}
	else 
	{
		this.document.all.mydiv1.className = "charrlose";
		this.document.all.mydiv1.className = "charr1";
		this.document.all.sample1.style.display="none";
	}
}

